package UniversityExamManagementSystem;

public class Student {
	String studentId;
	String name;
	int attendPer;
	
	public Student() {
		super();
	}

	public Student(String studentId, String name, int attendPer) {
		super();
		this.studentId = studentId;
		this.name = name;
		this.attendPer = attendPer;
	}
	
	

}
